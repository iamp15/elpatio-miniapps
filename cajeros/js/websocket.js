/**
 * M√≥dulo WebSocket para la app de cajeros
 */

class CajeroWebSocket {
  constructor() {
    this.socket = null;
    this.isConnected = false;
    this.isAuthenticated = false;
    this.userData = null;
    this.reconnectAttempts = 0;
    this.maxReconnectAttempts = 10; // M√°s intentos
    this.reconnectDelay = 1000; // Menos delay
    this.activeTransactionRooms = new Set(); // Track active transaction rooms
    this.lastAuthToken = null; // Store token for re-authentication
    this.callbacks = {
      onConnect: null,
      onDisconnect: null,
      onAuthResult: null,
      onNuevaSolicitudDeposito: null,
      onVerificarPago: null,
      onDepositoCompletado: null,
      onDepositoRechazado: null,
      onTransaccionCanceladaPorJugador: null,
      onError: null,
    };
  }

  /**
   * Conectar al servidor WebSocket
   */
  connect() {
    if (this.socket && this.isConnected) {
      return;
    }

    // Detectar URL del servidor
    // En producci√≥n, siempre usar Railway
    const isLocalhost =
      window.location.hostname === "localhost" ||
      window.location.hostname === "127.0.0.1";
    const socketUrl = isLocalhost
      ? "http://localhost:3001"
      : "https://elpatio-backend.fly.dev";

    // Conectando a WebSocket

    // Importar Socket.IO din√°micamente
    if (typeof io === "undefined") {
      console.error("Socket.IO no est√° cargado");
      return;
    }

    this.socket = io(socketUrl, {
      transports: ["websocket", "polling"],
      timeout: 30000, // M√°s tiempo para conectar
      forceNew: true,
      reconnection: true,
      reconnectionAttempts: 10,
      reconnectionDelay: 1000,
      reconnectionDelayMax: 5000,
      maxReconnectionAttempts: 10,
    });

    this.setupEventHandlers();
  }

  /**
   * Configurar manejadores de eventos
   */
  setupEventHandlers() {
    this.socket.on("connect", () => {
      this.isConnected = true;
      this.reconnectAttempts = 0; // Resetear intentos de reconexi√≥n

      // Re-autenticar autom√°ticamente si tenemos token guardado
      // Esto maneja tanto la conexi√≥n inicial como las reconexiones
      if (this.lastAuthToken && !this.isAuthenticated) {
        console.log("üîê [RECOVERY] Re-autenticando cajero autom√°ticamente...");
        setTimeout(() => {
          this.reauthenticateAndRejoinRooms();
        }, 500);
      }

      if (this.callbacks.onConnect) {
        this.callbacks.onConnect();
      }
    });

    this.socket.on("disconnect", (reason) => {
      console.log("‚ùå Desconectado del servidor WebSocket:", reason);
      this.isConnected = false;
      this.isAuthenticated = false;
      if (this.callbacks.onDisconnect) {
        this.callbacks.onDisconnect(reason);
      }
    });

    // Reconexi√≥n autom√°tica de Socket.IO
    this.socket.on("reconnect", (attemptNumber) => {
      console.log(`üîÑ Reconectado autom√°ticamente (intento ${attemptNumber})`);
      this.isConnected = true;
      this.reconnectAttempts = 0; // Resetear contador manual

      // Re-autenticar y re-unirse a rooms
      setTimeout(() => {
        this.reauthenticateAndRejoinRooms();
      }, 500);
    });

    this.socket.on("reconnect_attempt", (attemptNumber) => {
      console.log(`üîÑ Intento de reconexi√≥n autom√°tica ${attemptNumber}`);
    });

    this.socket.on("reconnect_error", (error) => {
      console.error("‚ùå Error en reconexi√≥n autom√°tica:", error);
    });

    this.socket.on("reconnect_failed", () => {
      console.error(
        "‚ùå Fall√≥ la reconexi√≥n autom√°tica, iniciando reconexi√≥n manual"
      );
      this.attemptReconnect();
    });

    this.socket.on("connect_error", (error) => {
      console.error("‚ùå Error de conexi√≥n WebSocket:", error);
      console.error("‚ùå Detalles del error:", {
        message: error.message,
        description: error.description,
        context: error.context,
        type: error.type,
      });

      // Intentar reconexi√≥n autom√°tica
      this.attemptReconnect();
    });

    this.socket.on("auth-result", (result) => {
      this.isAuthenticated = result.success;
      this.userData = result.success ? result.user : null;

      if (result.success) {
        console.log("‚úÖ [AUTH] Cajero autenticado:", result.user?.nombre);

        // Si hay informaci√≥n de recuperaci√≥n, procesarla
        if (result.recovery && result.recovery.transactionsRecovered) {
          console.log(
            `üîÑ [RECOVERY] ${result.recovery.transactionsRecovered.length} transacciones recuperadas autom√°ticamente`
          );
        }
      } else {
        console.error(
          "‚ùå [AUTH] Autenticaci√≥n de cajero fallida:",
          result.message
        );
      }

      if (this.callbacks.onAuthResult) {
        this.callbacks.onAuthResult(result);
      }
    });

    this.socket.on("nueva-solicitud-deposito", (data) => {
      if (this.callbacks.onNuevaSolicitudDeposito) {
        this.callbacks.onNuevaSolicitudDeposito(data);
      }
    });

    this.socket.on("verificar-pago", (data) => {
      // Filtrar por target: solo procesar si es para cajero
      if (data.target === "cajero" && this.callbacks.onVerificarPago) {
        this.callbacks.onVerificarPago(data);
      }
    });

    this.socket.on("deposito-completado", (data) => {
      // Filtrar por target: solo procesar si es para cajero
      if (data.target === "cajero" && this.callbacks.onDepositoCompletado) {
        this.callbacks.onDepositoCompletado(data);
      }
    });

    this.socket.on("deposito-rechazado", (data) => {
      // Filtrar por target: solo procesar si es para cajero
      if (data.target === "cajero" && this.callbacks.onDepositoRechazado) {
        this.callbacks.onDepositoRechazado(data);
      }
    });

    this.socket.on("transaccion-cancelada-por-timeout", (data) => {
      console.log("‚è±Ô∏è Transacci√≥n cancelada por timeout:", data);
      if (this.callbacks.onTransaccionCanceladaPorTimeout) {
        this.callbacks.onTransaccionCanceladaPorTimeout(data);
      }
    });

    this.socket.on("error", (error) => {
      console.error("‚ùå Error en WebSocket:", error);
      if (this.callbacks.onError) {
        this.callbacks.onError(error);
      }
    });

    // Evento de transacci√≥n cancelada por jugador
    this.socket.on("transaccion-cancelada-por-jugador", (data) => {
      console.log(
        "‚ùå [CANCELACION] Evento recibido - Jugador cancel√≥ transacci√≥n"
      );
      console.log("‚ùå [CANCELACION] Data completa:", data);
      console.log("‚ùå [CANCELACION] TransaccionId:", data.transaccionId);
      console.log(
        "‚ùå [CANCELACION] Callback existe:",
        !!this.callbacks.onTransaccionCanceladaPorJugador
      );

      if (this.callbacks.onTransaccionCanceladaPorJugador) {
        console.log("‚ùå [CANCELACION] Ejecutando callback...");
        this.callbacks.onTransaccionCanceladaPorJugador(data);
      } else {
        console.error("‚ùå [CANCELACION] Callback NO est√° configurado!");
      }
    });

    // Nuevos eventos de recuperaci√≥n
    this.socket.on("transaction-state-recovered", (data) => {
      console.log("‚úÖ [RECOVERY] Estado de transacci√≥n recuperado:", data);
      // El cajero puede ver el estado actual de transacciones recuperadas
    });

    this.socket.on("reconnection-successful", (data) => {
      console.log("‚úÖ [RECOVERY] Reconexi√≥n exitosa:", data);
      // Notificar al cajero que se recuperaron transacciones
    });

    this.socket.on("participant-disconnected", (data) => {
      console.log("‚ö†Ô∏è [RECOVERY] Participante desconectado:", data);
      if (data.tipo === "jugador") {
        console.log(
          `‚ö†Ô∏è Jugador desconectado en transacci√≥n ${data.transaccionId}`
        );
        // El cajero puede mostrar un indicador de que el jugador se desconect√≥
      }
    });

    this.socket.on("participant-reconnected", (data) => {
      console.log("‚úÖ [RECOVERY] Participante reconectado:", data);
      if (data.tipo === "jugador") {
        console.log(
          `‚úÖ Jugador reconectado en transacci√≥n ${data.transaccionId}`
        );
        // El cajero puede ocultar el indicador de desconexi√≥n
      }
    });

    this.socket.on("participant-disconnected-timeout", (data) => {
      console.log("‚ùå [RECOVERY] Participante no pudo reconectar:", data);
      if (data.tipo === "jugador") {
        console.log(
          `‚ùå Jugador no reconect√≥ en transacci√≥n ${data.transaccionId}`
        );
        // El cajero debe verificar el estado de la transacci√≥n manualmente
      }
    });
  }

  /**
   * Autenticar como cajero
   */
  authenticateCajero(token) {
    if (!this.isConnected) {
      console.error("No hay conexi√≥n WebSocket");
      return;
    }

    // Guardar token para reconexi√≥n
    this.lastAuthToken = token;

    console.log("üîê Autenticando cajero...");
    this.socket.emit("auth-cajero", {
      token,
    });
  }

  /**
   * Aceptar solicitud de dep√≥sito
   */
  aceptarSolicitud(transaccionId, transaccionData) {
    if (!this.isConnected || !this.isAuthenticated) {
      console.error("No hay conexi√≥n o no est√° autenticado");
      return;
    }

    // Trackear room de transacci√≥n
    this.activeTransactionRooms.add(transaccionId);

    console.log("‚úÖ Aceptando solicitud:", { transaccionId, transaccionData });
    this.socket.emit("aceptar-solicitud", {
      transaccionId,
      transaccionData,
    });
  }

  /**
   * Atender dep√≥sito
   */
  atenderDeposito(jugadorSocketId, depositoData) {
    if (!this.isConnected || !this.isAuthenticated) {
      console.error("No hay conexi√≥n o no est√° autenticado");
      return;
    }

    // Atendiendo dep√≥sito
    this.socket.emit("atender-deposito", {
      jugadorSocketId,
      ...depositoData,
    });
  }

  /**
   * Confirmar dep√≥sito
   */
  confirmarDeposito(jugadorSocketId, transaccionId) {
    if (!this.isConnected || !this.isAuthenticated) {
      console.error("No hay conexi√≥n o no est√° autenticado");
      return;
    }

    console.log("‚úÖ Confirmando dep√≥sito:", { jugadorSocketId, transaccionId });
    this.socket.emit("confirmar-deposito", {
      jugadorSocketId,
      transaccionId,
    });
  }

  /**
   * Rechazar dep√≥sito
   */
  rechazarDeposito(jugadorSocketId, motivo) {
    if (!this.isConnected || !this.isAuthenticated) {
      console.error("No hay conexi√≥n o no est√° autenticado");
      return;
    }

    console.log("‚ùå Rechazando dep√≥sito:", { jugadorSocketId, motivo });
    this.socket.emit("rechazar-deposito", {
      jugadorSocketId,
      motivo,
    });
  }

  /**
   * Confirmar pago (verificaci√≥n de pago)
   */
  confirmarPagoCajero(transaccionId, notas = null) {
    console.log(
      "üîç [WebSocket] confirmarPagoCajero llamado para transacci√≥n:",
      transaccionId
    );
    console.log("üîç [WebSocket] Estado conexi√≥n:", {
      isConnected: this.isConnected,
      isAuthenticated: this.isAuthenticated,
    });
    console.log("üîç [WebSocket] Stack trace:", new Error().stack);

    if (!this.isConnected || !this.isAuthenticated) {
      console.error("No hay conexi√≥n o no est√° autenticado");
      return;
    }

    console.log("‚úÖ [WebSocket] Enviando evento verificar-pago-cajero:", {
      transaccionId,
      accion: "confirmar",
      notas,
    });
    this.socket.emit("verificar-pago-cajero", {
      transaccionId,
      accion: "confirmar",
      notas,
    });
  }

  /**
   * Rechazar pago (verificaci√≥n de pago)
   */
  rechazarPagoCajero(transaccionId, motivo) {
    if (!this.isConnected || !this.isAuthenticated) {
      console.error("No hay conexi√≥n o no est√° autenticado");
      return;
    }
    console.log("‚ùå Rechazando pago:", { transaccionId, motivo });
    this.socket.emit("verificar-pago-cajero", {
      transaccionId,
      accion: "rechazar",
      motivo,
    });
  }

  /**
   * Intentar reconexi√≥n autom√°tica
   */
  attemptReconnect() {
    if (this.reconnectAttempts >= this.maxReconnectAttempts) {
      console.error("‚ùå M√°ximo n√∫mero de intentos de reconexi√≥n alcanzado");
      return;
    }

    this.reconnectAttempts++;
    console.log(
      `üîÑ Intentando reconexi√≥n ${this.reconnectAttempts}/${this.maxReconnectAttempts} en ${this.reconnectDelay}ms...`
    );

    setTimeout(() => {
      this.connect();

      // Despu√©s de conectar, re-autenticar y re-unirse a rooms
      setTimeout(() => {
        this.reauthenticateAndRejoinRooms();
      }, 1000);
    }, this.reconnectDelay);
  }

  /**
   * Re-autenticar y re-unirse a rooms despu√©s de reconexi√≥n
   */
  reauthenticateAndRejoinRooms() {
    if (!this.isConnected) {
      console.log("‚ö†Ô∏è No hay conexi√≥n para re-autenticaci√≥n");
      return;
    }

    // Re-autenticar si tenemos token guardado
    if (this.lastAuthToken) {
      console.log("üîê Re-autenticando despu√©s de reconexi√≥n...");
      this.authenticateCajero(this.lastAuthToken);

      // Re-unirse a rooms de transacciones activas
      setTimeout(() => {
        this.rejoinTransactionRooms();
      }, 500);
    }
  }

  /**
   * Re-unirse a rooms de transacciones activas
   */
  rejoinTransactionRooms() {
    if (this.activeTransactionRooms.size === 0) {
      console.log("üìã No hay rooms de transacciones activas para re-unirse");
      return;
    }

    console.log(
      `üîÑ Re-uni√©ndose a ${this.activeTransactionRooms.size} rooms de transacciones...`
    );

    for (const transaccionId of this.activeTransactionRooms) {
      console.log(`üìã Re-uni√©ndose a room de transacci√≥n: ${transaccionId}`);
      this.socket.emit("unirse-room-transaccion", { transaccionId });
    }
  }

  /**
   * Desconectar
   */
  disconnect() {
    if (this.socket) {
      this.socket.disconnect();
      this.socket = null;
      this.isConnected = false;
      this.isAuthenticated = false;
      this.userData = null;
      this.reconnectAttempts = 0;
    }
  }

  /**
   * Configurar callbacks
   */
  on(event, callback) {
    if (this.callbacks.hasOwnProperty(event)) {
      this.callbacks[event] = callback;
    } else {
      console.warn("Evento no reconocido:", event);
    }
  }

  /**
   * Obtener estado de conexi√≥n
   */
  getStatus() {
    return {
      isConnected: this.isConnected,
      isAuthenticated: this.isAuthenticated,
      userData: this.userData,
    };
  }
}

// Crear instancia global
window.cajeroWebSocket = new CajeroWebSocket();

// Exportar para uso en m√≥dulos
if (typeof module !== "undefined" && module.exports) {
  module.exports = CajeroWebSocket;
}
